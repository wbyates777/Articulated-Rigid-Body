BResults
forward( BModel model )
{
    // set gravity here; gravity is not saved in the .urdf file
    model.gravity( BVector3(0.0, -9.81, 0.0));

    BDynamics dyn;
    BModelState qinput(model);
    
    for (int i = 0; i < qinput.tau.size(); ++i) 
        qinput.tau[i] = 1.0;

    // forward dynamics (ABA) - calculate accelerations 'qddot' using 'tau' 
    dyn.forward(model, qinput);
    
    // extract results
    BResults res1 = BResultAnalysis::extract_acc(model, qinput);

    return res1;
}

