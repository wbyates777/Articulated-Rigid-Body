void
single_body( void ) 
{
    std::cout << "\nARB Example 3 - Single Body\n" << std::endl;
    // note spatial vectors - (angular,linear) - a force or velocity
    BModel* model = new BModel;

    model->gravity( BVector3(0.0, -9.81, 0.0) );
    //model->gravity( BVector3(0.0, 0.0, 0.0) );
  
    // set up single body -- i.e. a spaceship 
    // floating base only used for first body in model.
    BBody spaceship = BBody(BInertia(10.0,  B_ZERO_3, BVector3(1.0, 1.0, 1.0)));
    BJoint joint = BJoint( BJoint::FloatBase );
    BBodyId spaceshipId = model->addBody(0, arb::Xtrans(B_ZERO_3), joint, spaceship);
    
    // control -- apply myforce to whole spaceship model -- 100 Newtons along z axis
    // body[0] is a header, body[1] is virtual, body[2] is the first, real body with mass
    // for floating base we  only need to apply external force to body[2] (or body[spaceshipId])
    // external forces are assumed to be in world coordinates
    BVector6 myforce( B_ZERO_3, 0.0, 0.0, 100.0);
    BExtForce f_ext(model->numBody(), B_ZERO_6); 
    f_ext[spaceshipId] =  BVector6( myforce );
    
    BModelState qinput;
    
    // this apply to 'internal' model joints/bodies
    qinput.q.resize(model->qsize(),0.0);
    qinput.qdot.resize(model->qdotsize(), 0.0);
    qinput.tau.resize(model->qdotsize(), 0.0);
    
    BDynamics dyn;
    dyn.forward(*model, qinput, f_ext);
    
    std::cout << "linear acc (" << qinput.qddot[0] << ", " << qinput.qddot[1] << ", " << qinput.qddot[2] << std::endl;
    std::cout << "angular acc (" << qinput.qddot[3] << ", " << qinput.qddot[4] << ", " << qinput.qddot[5] << std::endl;
    
    std::cout << std::endl;
    
    delete model;
}


BRBInertia
sphere( double mass, double radius)
// set up  a sphere   
{
    double diag = ((2.0/5.0) * mass * (radius * radius));
    BMatrix3 I_o(diag); // rotational inertia at body coordinate frame origin
    
    BVector3 h(0.0);    // linear momentum
    
    return BRBInertia(mass, h, I_o);
}


BMatrix6 
toWorldInvInertia( BBody &body )
// convert 'body coordinate' inverse spatial inertia I^{-1}_b to 'world coordinates' I^{-1}_w 
// Note in a rotating body I_b is constant while I_w is changing 
// see RBDA, Table 2.5 and eqn 2.66, page 34
{
    const BTransform X(body.X_base().E()); // world to body
    return arb::inverse(X) *  arb::inverse(body.I()) * arb::dual(X);
}

void
impulse( void )
// RBDA, chapter 11, Contact and Impact. 
// Two-Body Collisions, page 232, eqn 11.65
/*
 ''two rigid bodies, body1 and body2, that collide at a single point C.
 Their velocities before the impact are v1 and v2, and their velocities afterwards
 are v1 + ∆v1 and v2 + ∆v2. We assume initially that there is no friction at
 the contact.''
 */
{
    std::cout << "\nARB Example 4 - Spatial Impulse\n" << std::endl;
    
    // body mass, radius
    BBody body1(sphere(100.0, 1.5));
    BBody body2(sphere(85.0, 1.5));

    // body velocity
    body1.v(BVector6(B_ZERO_3, 0.0, 0.0, 2.0));   // v1 moving along z-axis towards v2
    body2.v(BVector6(B_ZERO_3, 0.0, 0.0, -3.0));  // v2 moving along -z-axis towards v1

    // details of direct contact/collision between body1 and body2
    glm::dvec3 C(0.0, 0.0, 1.5);  // contact point in world coordinates
    glm::dvec3 n(0.0, 0.0, 1.0);  // contact normal (already normalized)
  
    // coefficient of restitution (0 -> inelastic, like clay, 1.0 -> elastic, like a billiard ball)
    BScalar e = 1.0;
    
    // n_s is the unit spatial impulse
    BVector6 n_s = BVector6(arb::cross(C, n), n); 
    
    // K or effective mass
    const BMatrix6 invI1 = toWorldInvInertia(body1);
    const BMatrix6 invI2 = toWorldInvInertia(body2);
    BScalar K = arb::dot(n_s, ((invI1 + invI2) * n_s));
    
    // current relative velocity (the separation velocity at contact i, eqn 11.62)
    BScalar ns_dot_vel = arb::dot(n_s, body2.v() - body1.v());
    
    // size or magnitude of impulse 
    BScalar j = (-(1.0 + e) * ns_dot_vel) / K;
    
    // apply spatial impulse to velocity
    const BVector6 impulse = n_s * j;
    body1.v() -= invI1 * impulse;
    body2.v() += invI2 * impulse;
    
    std::cout << "Body1 " <<  body1.v() << std::endl;
    std::cout << "Body2 " <<  body2.v() << std::endl << std::endl;
    
    // general 1D Newtonian elastic collision equations derived from the 
    // conservation of momentum and kinetic energy.
    // https://en.wikipedia.org/wiki/Elastic_collision
    BScalar mA = body1.I().mass();
    BScalar uA = 2.0; // body1.v()[5];
    BScalar mB = body2.I().mass();
    BScalar uB = -3.0; // body2.v()[5];
    BScalar vA = (((mA - mB) / (mA + mB)) * uA) + (((2.0 * mB) / (mA + mB)) * uB);
    BScalar vB = (((2.0 * mA) / (mA + mB)) * uA) + (((mB - mA) / (mA + mB)) * uB);
    
    std::cout << "Should equal " << "v1 " << vA  << ", v2 " <<  vB << std::endl;
}


void
collisions( void )
{
    std::cout << "\nARB Example 5 - Newton Euler and Collisions\n" << std::endl;
    BContactManager contact_manager;
    
    double mass1 = 100.0, radius1 = 1.5; 
    double mass2 = 80.0,  radius2 = 1.25;

    std::vector<CBody> bodies(2);
    bodies[0] = CBody(1, BBody(sphere(mass1, radius1)), BBox(radius1));
    bodies[1] = CBody(2, BBody(sphere(mass2, radius2)), BBox(radius2));


    bodies[0].pos(B_ZERO_3);
    bodies[1].pos(BVector3(0.0, 4.0, 0.0));
    
    bodies[1].force(BVector6(B_ZERO_3, 0.0, -2000.0, 0.0));
    
    std::vector<ABody*> body_collisions = { &bodies[0], &bodies[1] };
    
    double dt = 0.1;
    double T = 0.0;
    std::cout << "time \t\t" << "pos body" << bodies[0].objId();
    std::cout << "\t\t\t\t\t\t\t\t\tpos body" <<  bodies[1].objId() << std::endl;
    
    // semi-implicit Euler (integrate velocity first, then position)
    for (int i = 0; i < 8; ++i)
    {
        for (CBody &cb : bodies) // update all velocities
        {
            cb.updateVelocity(dt);
        }
        
        // check for collisions; resolve any that occur up by updating body velocities
        int num_contact = contact_manager.resolve(dt, body_collisions);
        
        if (num_contact)
        {
            std::cout << std::endl;
            std::cout << " >*BANG*< - Collision Detected!" << std::endl;
            
            //std::cout  <<  glm::degrees(bodies[0].v().ang()) << "\t"  <<  glm::degrees(bodies[1].v().ang()) << "\n";
            //std::cout << bodies[0].v().lin() << "\t"  <<  bodies[1].v().lin() << "\n";
            std::cout << std::endl;
        }
        
        for (CBody &cb : bodies)
        {
            cb.updatePosition(dt);
        }
        
        T += dt;
        std::cout << T << "\t"  <<  bodies[0].pos() << "\t***\t\t";
        std::cout << bodies[1].pos() << std::endl;
    }
}

BModelState 
getState( const BModel &model )
{
    BModelState qinput;
    qinput.q.resize(model.qsize(), BScalar(0.0));
    qinput.qdot.resize(model.qdotsize(), BScalar(0.0));
    qinput.tau.resize(model.qdotsize(), BScalar(0.0));
    qinput.q[0] = 0.5; // start the first joint at ~30 degrees
    qinput.q[1] = 0.5; // start the second joint at ~30 degrees
    
    return qinput; 
}

BModel
getModel( int &bodyId2, BScalar m2_guess )
{
    BModel model;

    model.gravity(BVector3(0.0, -9.81, 0.0));
    
    // Body 1 (fixed properties)
    BInertia I1(1.0, BVector3(0.0, -0.5, 0.0), BVector3(0.1));
    BJoint J1(BJoint::Revolute, BVector3(0.0, 0.0, 1.0)); 
    int bodyId1 = model.addBody(0, BTransform(B_ZERO_3), J1, BBody(I1));

    // Body 2 (uses our 'm2_guess' variable)
    BInertia I2(m2_guess, BVector3(0,-0.5,0), BVector3(0.1));
    BJoint J2(BJoint::Revolute, BVector3(0.0, 0.0, 1.0));
    bodyId2 = model.addBody(bodyId1, BTransform(BVector3(0.0, -1.0, 0.0)), J2, BBody(I2) );
    
    return model;
}

void
propagate(BModel &model, BModelState &qinput)
// execute the model for 200 iterations
{
    BDynamics dyn;
    BScalar dt(0.01);
    for (int i = 0; i < 200; ++i) 
    {
        dyn.forward( model, qinput );
        
        for (int j = 0; j < qinput.q.size(); ++j) 
        {
            qinput.q[j] += qinput.qdot[j] * dt;
            qinput.qdot[j] += qinput.qddot[j] * dt;
        }
    }
}

BVector3 
getTarget(BScalar m2) 
// compute the target position given m2 
{
    int bodyId2;
 
    BModel model = getModel(bodyId2, m2);
    BModelState qinput = getState(model);
    propagate(model, qinput);
    BVector3 target_end_effector_pos = model.toBasePos(bodyId2);
    
    return target_end_effector_pos;
}

void 
double_pendulum_system_id(void) 
// Example: Trajectory Matching - Recovering Hidden Mass
// A common application of AD is Trajectory Matching, that is recovering the mass of 
// a system by observing its trajectory. In a double-pendulum benchmark, the task 
// is to find the true mass of a body (m_2=2.0) starting from a faulty guess (m_2 = 5.0.) 
{
    std::cout << "\nARB Example 6 - Trajectory Matching\n" << std::endl;
    
    int bodyId2;

    // target and target state 
    BScalar m2_actual = 2.0; // you can vary this a bit, but not too much as other body has mass 1.0.
    BVector3 target_end_effector_pos = getTarget(m2_actual); 
   
    // mass_2 is parameter we want to recover - initial (poor) guess
    BScalar m2_guess = 5.0;
    
    double learnRate = 2.5; 
    for (int epoch = 0; epoch < 50; ++epoch) 
    {
        m2_guess[1] = 1.0; // setup sensitivity
        
        BModel model = getModel(bodyId2, m2_guess);
        BModelState qinput = getState(model);
    
        // propagate model using m2_guess
        propagate(model, qinput);

        // final position of the bodyId2 
        BVector3 current_pos = model.toBasePos(bodyId2);

        // the loss function
        BScalar loss = arb::dot(current_pos - target_end_effector_pos, 
                                current_pos - target_end_effector_pos);

        //  gradient descent 
        double grad = autodiff::derivative(loss);
        
        // check if grad is zero
        if (grad == 0.0) 
        {
            std::cout << "WARNING: Gradient is zero - stopping at epoch " << epoch << std::endl;
            return;
        }
        
        m2_guess -= learnRate * grad;

        std::cout << "Epoch " << epoch << " - m2 is " << m2_guess << " - loss: " << loss << std::endl;
    }
}


